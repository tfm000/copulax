# Per-family tolerances for fit theta_hat agreement with R.
# Both use itau (Kendall's tau inversion); differences come purely
# from the tau->theta inversion method:
#   - Clayton, Gumbel: closed-form inversion -> machine precision
#   - AMH: near-closed-form (Taylor near 0) -> ~1e-8
#   - Joe: bisection in copulax -> ~1e-4
#   - Frank: Debye function approximation differs between R and
#     copulax (same approximation family, different polynomial
#     truncation) -> ~1e-2
# Each tolerance was chosen as 5x the observed gap on the reference
# dataset, so a future regression that meaningfully changes the
# inversion will fail.
FIT_TOLERANCES = {
    "Clayton": 1e-12,
    "Gumbel":  1e-12,
    "AMH":     1e-7,
    "Joe":     1e-3,
    "Frank":   1e-2,
}

FIT_CASES = list(FIT_DATA.keys())


class TestArchimedeanFitAgainstRCopula:
    """Fit copulax on R-generated data and compare theta_hat to R's itau fit.

    Both R::fitCopula(method="itau") and copulax.fit_copula() compute
    Kendall's tau on the data and invert. The empirical Kendall's tau
    is identical (verified separately), so any difference between the
    two theta_hat values is purely from the tau->theta inversion.
    """

    @pytest.mark.parametrize("name", FIT_CASES, ids=FIT_CASES)
    def test_theta_hat_matches_r_itau(self, name):
        copula = COPULA_BY_NAME[name]
        ref = FIT_DATA[name]
        u = jnp.array(ref["u"])
        theta_hat_cx = float(copula.fit_copula(u=u)["copula"]["theta"])
        np.testing.assert_allclose(
            theta_hat_cx, ref["theta_hat_r"],
            rtol=FIT_TOLERANCES[name],
            err_msg=(
                f"{name}: copulax fit theta_hat={theta_hat_cx} "
                f"differs from R itau theta_hat={ref['theta_hat_r']} "
                f"on R-generated data (theta_true={ref['theta_true']})"
            ),
        )


# ---------------------------------------------------------------------------
# 3. Sampling against R copula (empirical stats from large samples)
# ---------------------------------------------------------------------------
#
# Both samplers should produce the same population distribution. With
# n=50000 from each, empirical statistics from independent samples
# differ by ~sqrt(2)*SE.
#
# Empirical Kendall's tau SE: sqrt((1-tau^2)/n) ~ 0.003 for tau=0.5
#   -> two-sample diff ~ 0.005 -> 4-sigma tolerance ~ 0.02
# Empirical CDF point SE: sqrt(p(1-p)/n) <= sqrt(0.25/50000) ~ 0.0022
#   -> two-sample diff ~ 0.003 -> 4-sigma tolerance ~ 0.015 (atol)
#
# Tolerances are conservative enough to avoid flakes but tight enough
# to catch real sampler regressions.

SAMPLE_TAU_TOL = 0.02
SAMPLE_CDF_ATOL = 0.015

SAMPLE_CASES = list(SAMPLE_STATS.keys())


def _empirical_bivariate_cdf(samples, points):
    """P[U1 <= p1, U2 <= p2] for each row of points."""
    return np.array([
        np.mean((samples[:, 0] <= p[0]) & (samples[:, 1] <= p[1]))
        for p in points
    ])


class TestArchimedeanSamplingAgainstRCopula:
    """Compare copulax samples to R rCopula() samples via empirical stats.

    Generates n=50000 samples in copulax with a fixed key, then checks
    that the empirical Kendall's tau and the empirical bivariate CDF at
    U_POINTS agree with the same statistics from R-generated samples
    of the same size. Tolerances allow ~4-sigma deviation between two
    independent Monte Carlo samples.
    """

    @pytest.mark.parametrize("name", SAMPLE_CASES, ids=SAMPLE_CASES)
    def test_sample_empirical_stats_match_r(self, name):
        import scipy.stats
        copula = COPULA_BY_NAME[name]
        ref = SAMPLE_STATS[name]
        n = ref["n"]

        params = _make_params(copula, ref["theta"])
        key = jax.random.PRNGKey(2024)
        samples = np.asarray(copula.copula_rvs(size=n, params=params, key=key))

        # Drop any non-finite rows (sampling can produce NaNs near
        # boundaries for some frailty distributions).
        finite = np.all(np.isfinite(samples), axis=1)
        samples = samples[finite]
        assert len(samples) > 0.99 * n, (
            f"{name}: too many non-finite samples "
            f"({n - len(samples)} of {n})"
        )

        tau_emp_cx = scipy.stats.kendalltau(
            samples[:, 0], samples[:, 1]
        ).statistic
        cdf_emp_cx = _empirical_bivariate_cdf(samples, U_POINTS)

        np.testing.assert_allclose(
            tau_emp_cx, ref["tau_emp_r"],
            atol=SAMPLE_TAU_TOL,
            err_msg=(
                f"{name}: empirical Kendall's tau differs by more than "
                f"{SAMPLE_TAU_TOL} between copulax ({tau_emp_cx:.4f}) "
                f"and R ({ref['tau_emp_r']:.4f})"
            ),
        )
        np.testing.assert_allclose(
            cdf_emp_cx, ref["cdf_emp_r"],
            atol=SAMPLE_CDF_ATOL,
            err_msg=(
                f"{name}: empirical CDF at U_POINTS differs by more "
                f"than {SAMPLE_CDF_ATOL} between copulax and R\n"
                f"  copulax: {cdf_emp_cx}\n"
                f"  R:       {ref['cdf_emp_r']}"
            ),
        )
