def _make_params(copula, theta):
    """Build a bivariate params dict with the supplied theta."""
    params = copula.example_params(dim=2)
    params["copula"]["theta"] = jnp.array(float(theta))
    return params


def _copulax_cdf(copula, theta, u=U_POINTS):
    return np.asarray(
        copula.copula_cdf(u=jnp.array(u), params=_make_params(copula, theta))
    ).flatten()


def _copulax_pdf(copula, theta, u=U_POINTS):
    return np.asarray(
        copula.copula_pdf(u=jnp.array(u), params=_make_params(copula, theta))
    ).flatten()


