# ---------------------------------------------------------------------------
# 1. R copula package reference (CDF + PDF)
# ---------------------------------------------------------------------------

class TestArchimedeanAgainstRCopula:
    """Compare copulax CDF/PDF against hardcoded R copula v1.1.2 values.

    The R `copula` package is the de-facto reference implementation in
    statistics research (used by McNeil, Hofert, et al). All five
    Archimedean families covered.
    """

    @pytest.mark.parametrize("name,theta", R_CASES, ids=R_IDS)
    def test_cdf_matches_r(self, name, theta):
        copula = COPULA_BY_NAME[name]
        expected = R_REFERENCE_DATA[(name, theta)]["cdf"]
        actual = _copulax_cdf(copula, theta)
        np.testing.assert_allclose(
            actual, expected, rtol=1e-10, atol=1e-12,
            err_msg=f"{name} theta={theta} CDF mismatch vs R copula",
        )

    @pytest.mark.parametrize("name,theta", R_CASES, ids=R_IDS)
    def test_pdf_matches_r(self, name, theta):
        copula = COPULA_BY_NAME[name]
        expected = R_REFERENCE_DATA[(name, theta)]["pdf"]
        actual = _copulax_pdf(copula, theta)
        # PDF tolerance looser because copulax uses autodiff for the
        # generic path while R uses hand-coded closed forms; rounding
        # accumulates differently.
        np.testing.assert_allclose(
            actual, expected, rtol=1e-6, atol=1e-10,
            err_msg=f"{name} theta={theta} PDF mismatch vs R copula",
        )


